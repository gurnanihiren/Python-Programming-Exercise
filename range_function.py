

print('python range with one positional Argument')
for i in range(10):
    print(i,end=" ")
print(end='\n')

for i in range(5):
    print(i,end=" ")
print(end='\n')

for i in range(-5):
    print(i,end=" ")
print(end="\n")

for i in range(-10,-20):
    print(i,end=" ")
print(end="\n")

print('python range with two positional Argument')
for i in range(1,10):
    print(i,end=" ")
print(end="\n")


for i in range(2,20):
    print(i,end=" ")
print(end="\n")

print('python range with three positional Arguments')
for i in range(1,10,2):
    print(i,end=" ")
print(end="\n")


for i in range(0,50,5):
    print(i,end=" ")
print(end="\n")

for i in range(0,20,-2):
    print(i,end=" ")

for i in range(0,30,-5):
    print(i,end=" ")
print('\n')

print('python range with reverse range')

for i in range(10,0,-1):
    print(i,end=" ")
print('\n')

for i in range(20,0,-1):
    print(i,end=" ")
print('\n')

for i in range(5,0):
    print(i,end=" ")
print('\n')

for i in range(-10,0,2):
    print(i,end=" ")
print('\n')

for i in range(-10,0,-2):
    print(i,end=" ")
print('\n')


print('End parameter in Python')
for i in range(0,11):
    print(i,end="$")
print('\n')

for i in range(0,5):
    print(i,end="#")
print('\n')

for i in range(0,7):
    print(i,end="Hiren")
print('\n')

